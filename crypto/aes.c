extern int8_t aes(Crypto *params) {
    if (params->option != ENCRYPT_OPTION && params->option != DECRYPT_OPTION) {
        return 1;
    }
    if (params->keysize != 16 && params->keysize != 24 && params->keysize != 32) {
        return 2;
    }

    const uint8_t Nb = 4; // standart
    const uint8_t Nk = params->keysize / Nb;
    const uint8_t Nr = (params->keysize == 16) ? 10 : ((params->keysize == 24) ? 12 : 14);
    const uint8_t Nw = Nb * (Nr + 1);

    uint32_t Wkey[Nw];
    _key_expansion(Wkey, params->key, Nb, Nk, Nr, Nw);

    uint8_t block[16];
    
    switch(params->option) {
        case ENCRYPT_OPTION: {
            _copy_transpose_block(block, params->from);
            _add_round_key(block, Wkey);
            for (uint8_t round = 1; round <= Nr; ++round) {
                _sub_bytes(block);
                _shift_rows(block);
                if (round < Nr) {
                    _mix_columns(block);
                }
                _add_round_key(block, Wkey + (Nb * round));
            }
            _copy_transpose_block(params->to, block);
        }
        break;
        case DECRYPT_OPTION: {
            _copy_transpose_block(block, params->from);
            _add_round_key(block, Wkey + (Nb * Nr));
            for (int8_t round = Nr - 1; round >= 0; --round) {
                _inv_shift_rows(block);
                _inv_sub_bytes(block);
                _add_round_key(block, Wkey + (Nb * round));
                if (round > 0) {
                    _inv_mix_columns(block);
                }
            }
            _copy_transpose_block(params->to, block);
        }
        break;
    }
    
    return 0;
}

static void _key_expansion(uint32_t * Wkey, uint8_t * key, uint8_t Nb, uint8_t Nk, uint8_t Nr, uint8_t Nw) {
    for (uint8_t i = 0; i < Nk; ++i) {
        Wkey[i] = join_8bits_to_32bits(key + (4 * i));
    }
    uint32_t T;
    for (size_t i = Nk; i < Nw; ++i) {
        T = Wkey[i-1];
        if (i % Nk == 0) {
            T = _sub_word(_rot_word(T)) ^ Rcon[i/Nk];
        } else if (Nk == 8 && (i % Nk) == 4) {
            T = _sub_word(T);
        } 
        Wkey[i] = Wkey[i-Nk] ^ T;
    }
}

static uint32_t _sub_word(uint32_t word) {
    uint8_t bytes[4];
    split_32bits_to_8bits(word, bytes);
    for (uint8_t i = 0; i < 4; ++i) {
        bytes[i] = Sbox[bytes[i]];
    }
    return join_8bits_to_32bits(bytes);
}

static uint32_t _rot_word(uint32_t word) {
    uint8_t bytes[4];
    split_32bits_to_8bits(word, bytes);
    _shiftl_array(bytes, 4, 1);
    return join_8bits_to_32bits(bytes);
}

static void _copy_transpose_block(uint8_t * to, uint8_t * from) {
    for (uint8_t i = 0; i < 4; ++i) {
        for (uint8_t j = 0; j < 4; ++j) {
            to[i * 4 + j] = from[j * 4 + i];
        }
    }
}

static void _copy_block(uint8_t * to, uint8_t * from) {
    for (uint8_t i = 0; i < 4; ++i) {
        for (uint8_t j = 0; j < 4; ++j) {
            to[i * 4 + j] = from[i * 4 + j];
        }
    }
}

static void _inv_sub_bytes(uint8_t * block) {
    for (uint8_t i = 0; i < 16; ++i) {
        block[i] = InvSbox[block[i]];
    }
}

static void _sub_bytes(uint8_t * block) {
    for (uint8_t i = 0; i < 16; ++i) {
        block[i] = Sbox[block[i]];
    }
}

static void _inv_shift_rows(uint8_t * block) {
    for (uint8_t i = 1; i < 4; ++i) {
        _shiftr_array(block + (i * 4), 4, i);
    }
}

static void _shift_rows(uint8_t * block) {
    for (uint8_t i = 1; i < 4; ++i) {
        _shiftl_array(block + (i * 4), 4, i);
    }
}

// [14 11 13 9]
// [9 14 11 13]
// [13 9 14 11]
// [11 13 9 14]
static void _inv_mix_columns(uint8_t * block) {
    uint8_t columns[16];
    _copy_block(columns, block);
    for (uint8_t i = 0; i < 4; ++i) {
        block[0*4+i] = gf8_mul(0x0E, columns[0*4+i]) ^ gf8_mul(0x0B, columns[1*4+i]) ^ gf8_mul(0x0D, columns[2*4+i]) ^ gf8_mul(0x09, columns[3*4+i]);
        block[1*4+i] = gf8_mul(0x09, columns[0*4+i]) ^ gf8_mul(0x0E, columns[1*4+i]) ^ gf8_mul(0x0B, columns[2*4+i]) ^ gf8_mul(0x0D, columns[3*4+i]);
        block[2*4+i] = gf8_mul(0x0D, columns[0*4+i]) ^ gf8_mul(0x09, columns[1*4+i]) ^ gf8_mul(0x0E, columns[2*4+i]) ^ gf8_mul(0x0B, columns[3*4+i]);
        block[3*4+i] = gf8_mul(0x0B, columns[0*4+i]) ^ gf8_mul(0x0D, columns[1*4+i]) ^ gf8_mul(0x09, columns[2*4+i]) ^ gf8_mul(0x0E, columns[3*4+i]);
    }
}

// [2 3 1 1]
// [1 2 3 1]
// [1 1 2 3]
// [3 1 1 2]
static void _mix_columns(uint8_t * block) {
    uint8_t columns[16];
    _copy_block(columns, block);
    for (uint8_t i = 0; i < 4; ++i) {
        block[0*4+i] = gf8_mul(0x02, columns[0*4+i]) ^ gf8_mul(0x03, columns[1*4+i]) ^ columns[2*4+i] ^ columns[3*4+i];
        block[1*4+i] = columns[0*4+i] ^ gf8_mul(0x02, columns[1*4+i]) ^ gf8_mul(0x03, columns[2*4+i]) ^ columns[3*4+i];
        block[2*4+i] = columns[0*4+i] ^ columns[1*4+i] ^ gf8_mul(0x02, columns[2*4+i]) ^ gf8_mul(0x03, columns[3*4+i]);
        block[3*4+i] = gf8_mul(0x03, columns[0*4+i]) ^ columns[1*4+i] ^ columns[2*4+i] ^ gf8_mul(0x02, columns[3*4+i]);
    }
}

static void _add_round_key(uint8_t * block, uint32_t * Wkeys) {
    uint8_t bytes[4];
    for (uint8_t i = 0; i < 4; ++i) {
        split_32bits_to_8bits(Wkeys[i], bytes);
        for (uint8_t j = 0; j < 4; ++j) {
            block[j * 4 + i] ^= bytes[j];
        }
    }
}

static void _shiftr_array(uint8_t * array, size_t length, size_t shift) {
    uint8_t temp;
    size_t index;
    while (shift--) {
        temp = array[length-1];
        for (index = length-1; index > 0; index--)
            array[index] = array[index-1];
        array[0] = temp;
    }
}

static void _shiftl_array(uint8_t * array, size_t length, size_t shift) {
    uint8_t temp;
    size_t index;
    while(shift--) {
        temp = array[0];
        index = 1;
        for (; index < length; ++index) {
            array[index-1] = array[index];
        }
        array[index-1] = temp;
    }
}
